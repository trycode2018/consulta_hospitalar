    {
        //
        $paciente = $this->create();
        $paciente->nome_completo = $request->input('nome');
        $paciente->data_nascimento = $request->input('data_nascimento');
        $paciente->n_bi = $request->input('bi');
        $paciente->sexo = $request->input('sexo');
        $paciente->endereco = $request->input('endereco');
        $paciente->n_sequencial = 0;
        $paciente->save();
        return $paciente->id;
    }

